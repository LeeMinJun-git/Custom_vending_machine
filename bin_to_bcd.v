// ==============================================================================
// 서브 모듈 3 : bin_to_bcd (이진수 -> 십진수 변환기)
// 설명    : 컴퓨터가 쓰는 이진수 값(예: 1200)을 사람 눈에 보이는 각 자릿수의
//           십진수(1, 2, 0, 0)로 쪼개주는 아주 중요한 알고리즘(Double Dabble) 모듈입니다.
// ==============================================================================
module bin_to_bcd (
input [13:0] bin,     // FSM이 넘겨준 투입 금액 (최대 16383까지 표현 가능한 14비트)
output reg [3:0] th,  // 천(Thousands)의 자리 값
output reg [3:0] hu,  // 백(Hundreds)의 자리 값
output reg [3:0] te,  // 십(Tens)의 자리 값
output reg [3:0] on   // 일(Ones)의 자리 값
);

integer i;
reg [13:0] temp_bin; // 시프트(밀어내기) 연산을 위한 임시 공간
reg [15:0] bcd;      // 변환된 4개의 자릿수(4비트 x 4)가 저장될 최종 창고

// Double Dabble (Shift and Add 3) 알고리즘
// 비트를 왼쪽으로 한 칸씩 밀어내면서, 5 이상이 된 자릿수에는 3을 더해주는 규칙
always @(bin) begin
    bcd = 16'd0;
    temp_bin = bin;

    // 14비트 데이터이므로 비트 수만큼 14번 반복
    for (i = 0; i < 14; i = i + 1) begin
        // 시프트 하기 전에 각 자릿수(4비트)가 5 이상이면 3을 더함 (Add 3)
        if (bcd[3:0] >= 5)   bcd[3:0]   = bcd[3:0] + 3;   // 일의 자리 검사
        if (bcd[7:4] >= 5)   bcd[7:4]   = bcd[7:4] + 3;   // 십의 자리 검사
        if (bcd[11:8] >= 5)  bcd[11:8]  = bcd[11:8] + 3;  // 백의 자리 검사
        if (bcd[15:12] >= 5) bcd[15:12] = bcd[15:12] + 3; // 천의 자리 검사

        // 모든 bcd 비트를 왼쪽으로 한 칸씩 밀고(Shift), 맨 오른쪽 빈칸에 temp_bin의 가장 앞 비트를 끌어옴
        bcd = {bcd[14:0], temp_bin[13]};

        // temp_bin도 다음 비트를 넘겨주기 위해 왼쪽으로 한 칸 밈
        temp_bin = temp_bin << 1;
    end

    // 14번의 믹스 작업이 끝나면, 각 위치에 십진수 자릿수가 예쁘게 정리되어 있음
    th = bcd[15:12];
    hu = bcd[11:8];
    te = bcd[7:4];
    on = bcd[3:0];
end

endmodule
