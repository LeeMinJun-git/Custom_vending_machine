// ==============================================================================
// 서브 모듈 2 : decoder_7seg (7-Segment LED 폰트 사전)
// 설명    : 4비트 숫자(0~15)를 받아서, 실제 LED 불빛 모양에 맞는 7가닥의 켜짐/꺼짐
//           신호로 번역해 주는 글씨체(Font) 사전입니다. (Common Anode 방식 기준)
// ==============================================================================
module decoder_7seg (
input [3:0] hex_value, // 번역할 4비트 데이터 (0~9, A~F 등)
input [3:0] b,         // 현재 켤 자릿수 스위치 (1인 곳에 불이 들어옴)
output reg [6:0] seg,  // A~G 세그먼트 LED 제어선 (0일 때 불이 켜짐)
output reg [3:0] an    // 4개 FND 중 켤 곳의 전원 선 (0일 때 전원 공급됨)
);

always @(hex_value, b) begin
    an = ~b; // Common Anode 방식이라 0을 줘야 켜지므로 반전(~)을 시켜줌

    // 각 숫자/문자에 해당하는 LED 모양 (비트 배열: g, f, e, d, c, b, a 순서)
    // 0이면 불이 켜지고, 1이면 불이 꺼짐.
    case (hex_value)
        // 숫자 폰트 (0 ~ 9)
        4'b0000 : seg = 7'b1000000; // 0
        4'b0001 : seg = 7'b1111001; // 1
        4'b0010 : seg = 7'b0100100; // 2
        4'b0011 : seg = 7'b0110000; // 3
        4'b0100 : seg = 7'b0011001; // 4
        4'b0101 : seg = 7'b0010010; // 5
        4'b0110 : seg = 7'b0000010; // 6
        4'b0111 : seg = 7'b1111000; // 7
        4'b1000 : seg = 7'b0000000; // 8
        4'b1001 : seg = 7'b0011000; // 9

        // 커스텀 영문자 폰트 (LESS, donE, nonE, End 출력을 위해 특별히 만듦)
        4'd10 : seg = 7'b1000111; // 10(A) = 'L' 모양
        4'd11 : seg = 7'b0000110; // 11(B) = 'E' 모양
        4'd12 : seg = 7'b1111111; // 12(C) = '빈칸' (End 글자 뒤의 4번째 자리를 끄기 위해 할당)
        4'd13 : seg = 7'b0100001; // 13(D) = 소문자 'd' 모양
        4'd14 : seg = 7'b0100011; // 14(E) = 소문자 'o' 모양
        4'd15 : seg = 7'b0101011; // 15(F) = 소문자 'n' 모양

        default : seg = 7'b1111111; // 알 수 없는 값은 모든 LED 다 끄기 (1111111)
    endcase
end

endmodule
